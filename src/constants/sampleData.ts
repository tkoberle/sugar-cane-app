export const calculateTotalArea = (): number => {
  return samplePlots.reduce((total, plot) => total + plot.area, 0);
};

export const getPlotsByStatus = (status: string) => {
  return samplePlots.filter(plot => plot.status === status);
};

export const getPlotsByCycle = (cycle: number) => {
  return samplePlots.filter(plot => plot.currentCycle === cycle);
};